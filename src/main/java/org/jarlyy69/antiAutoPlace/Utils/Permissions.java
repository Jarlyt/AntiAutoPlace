package org.jarlyy69.antiAutoPlace.Utils;

/**
 * Class containing permission string constants used by the plugin
 */
public class Permissions {
    /**
     * Permission to bypass automatic place/checks
     * Players with this permission are generally exempt from detection
     */
    public static String BYPASS = "antiautoplace.bypass";

    /**
     * Permission to receive alerts/notifications about detections
     * Players with this permission will be notified when a cheater is detected
     */
    public static String ALERTS = "antiautoplace.alerts";
}
